package yoon;

public class FormatString {
    public static void main(String [] args){
        int age = 20;
        double height = 178.2;
        String name = "kim jimmy";

        System.out.printf(" name : %s \n", name);
        System.out.printf(" age : %d \n height : %e \n\n", age, height);
    }
}
