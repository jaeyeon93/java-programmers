package jaemoon;

public class lab1_2 {
    public static void main(String [] args){
        int x = 5;
        int y = 4;
        double z = 4.0;

        System.out.println(x/y*z); // result is 4.0
        System.out.println(x*z/y); // result is 5.0

        /*
        1st
        x/y => 1, 1*z = 4.0;
        2nd
        x*z => 20.0, 20.0/y = 5.0;
         */
    }
}
