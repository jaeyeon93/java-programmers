package algorithm;

import java.util.Scanner;

//https://www.acmicpc.net/problem/10951
public class Baekjoon10951 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        while (sc.hasNext()) {
            int a = sc.nextInt();
            int b = sc.nextInt();
            System.out.println(a + b);
        }
    }
}
